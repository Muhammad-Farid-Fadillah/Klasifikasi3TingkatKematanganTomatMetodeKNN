def extract_features(img):
    mask = np.any(img != [0,0,0], axis=-1)
    pixels = img[mask] if np.any(mask) else img.reshape(-1,3)

    hsv = cv2.cvtColor(img, cv2.COLOR_BGR2HSV)
    hsv_pixels = hsv[mask]

    hist = cv2.calcHist([hsv], [0,1,2], mask.astype(np.uint8),
                        [8,8,8], [0,256,0,256,0,256])
    hist = cv2.normalize(hist, hist).flatten()

    mean_rgb = np.mean(pixels, axis=0)
    mean_hsv = np.mean(hsv_pixels, axis=0)

    gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
    gray_pixels = gray[mask]

    brightness = np.mean(gray_pixels)
    std_color = np.std(pixels)
    saturation = np.mean(hsv_pixels[:,1])

    edges = cv2.Canny(gray, 100, 200)
    edge_density = np.mean(edges[mask])

    extra = np.array([brightness, std_color, saturation, edge_density])

    glcm = graycomatrix(gray, distances=[1,2],
                        angles=[0, np.pi/4],
                        levels=256, symmetric=True, normed=True)

    texture = np.array([
        graycoprops(glcm, 'contrast').mean(),
        graycoprops(glcm, 'homogeneity').mean(),
        graycoprops(glcm, 'energy').mean(),
        graycoprops(glcm, 'correlation').mean()
    ])

    return np.hstack([hist, mean_rgb, mean_hsv, extra, texture])

