def show_knn_3_samples(k=5):
    label_names = ["Tidak Matang", "Matang", "Busuk"]

    indices = []

    # ambil 1 dari tiap kelas
    for label in [0, 1, 2]:
        for i in range(len(y_test)):
            if y_test[i] == label:
                indices.append(i)
                break
    fig = plt.figure(figsize=(15,10))

    for idx, index in enumerate(indices):
        test_point = X_test[index]

        distances = []
        for i in range(len(X_train)):
            dist = np.linalg.norm(test_point - X_train[i])
            distances.append((i, dist, y_train[i]))

        distances = sorted(distances, key=lambda x: x[1])
        neighbors = distances[:k]

        df = pd.DataFrame(neighbors, columns=["Index", "Distance", "Label"])
        df["Distance"] = df["Distance"].apply(lambda x: round(x, 3))
        df["Label"] = df["Label"].apply(lambda x: label_names[x])

        votes = [n[2] for n in neighbors]
        unique, counts = np.unique(votes, return_counts=True)
        final = unique[np.argmax(counts)]
        actual = y_test[index]

        # gambar
        ax_img = fig.add_subplot(3,2,idx*2+1)
        ax_img.imshow(cv2.cvtColor(img_test[index], cv2.COLOR_BGR2RGB))

        color = "green" if final == actual else "red"

        ax_img.set_title(
            f"{idx+1}. Pred: {label_names[final]} | Actual: {label_names[actual]}",
            color=color
        )
        ax_img.axis('off')

        # tabel
        ax_tbl = fig.add_subplot(3,2,idx*2+2)
        ax_tbl.axis('off')

        table = ax_tbl.table(
            cellText=df.values,
            colLabels=df.columns,
            loc='center',
            cellLoc='center'
        )

        table.auto_set_font_size(False)
        table.set_fontsize(10)
        table.scale(1,1.5)

    plt.suptitle("Visualisasi Proses KNN (3 Data Test)", fontsize=16)
    plt.tight_layout()
    plt.show()


# ✅ panggil di sini (di luar fungsi)
